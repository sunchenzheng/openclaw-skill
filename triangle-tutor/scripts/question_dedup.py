# 相似度阈值（同一难度级别内）
SIMILARITY_THRESHOLDS = {
    "⭐": 0.80,     # 低难度：允许较高相似度（可以替换）
    "⭐⭐": 0.75,
    "⭐⭐⭐": 0.70,  # 压轴题：70%相似即判定重复
    "⭐⭐⭐⭐": 0.60, # 竞赛题：鼓励变式，60%即重复
    "⭐⭐⭐⭐⭐": 0.60,
}

# 题目关键词特征提取
def extract_features(question_text: str) -> Dict:
    """从题目文本中提取关键特征，用于相似度计算"""
    text = question_text.lower()
    
    features = {
        "angles": [],      # 角度值列表
        "side_lengths": [], # 边长/数字
        "models": [],     # 几何模型关键词
        "conclusions": [], # 结论类型
        "conditions": [],  # 条件类型
    }
    
    # 提取角度
    angle_patterns = [
        r'∠[abcABC]\s*[=:]\s*(\d+)°?',
        r'(\d+)\s*°',  
        r'∠\w+\s*[=:]\s*(\d+)',
    ]
    for p in angle_patterns:
        matches = re.findall(p, text)
        features["angles"].extend([int(m) for m in matches if int(m) <= 180])
    
    # 提取数字（边长）
    num_patterns = [r'(\d+(?:\.\d+)?)', r'边长[=:]\s*(\d+)', r'=\s*(\d+(?:\.\d+)?)']
    for p in num_patterns:
        matches = re.findall(p, text)
        features["side_lengths"].extend([float(m) for m in matches])
    
    # 几何模型关键词
    model_keywords = [
        '全等', '相似', '等腰', '等边', '直角', '勾股', '三线合一', '角平分线',
        '中线', '高', '中位线', '垂线', '平行', '旋转', '翻折', '对称',
        '将军饮马', '胡不归', '阿氏圆', '费马点', '手拉手', '沙漏', '奔驰',
        '12345', '猪蹄', '鹰嘴', '鸡腿', '内切圆', '外接圆', '内心', '外心', '重心',
        '面积', '周长', '求证', '求', '证明', '角度', '边', '长度', '比', '比例',
        '最大', '最小', '最值', '动点', '存在', '不存在',
    ]
    for kw in model_keywords:
        if kw in text:
            features["models"].append(kw)
    
    # 结论关键词
    conclusion_keywords = [
        '求证', '证明', '求', '计算', '判断', '求证', '∠', '=', '长度', '比', 
        '距离', '面积', '最大值', '最小值', '是否存在',
    ]
    for kw in conclusion_keywords:
        if kw in text:
            features["conclusions"].append(kw)
    
    return features


def text_similarity(text1: str, text2: str) -> float:
    """计算两道题目的文本相似度（0-1）"""
    return SequenceMatcher(None, text1, text2).ratio()


def feature_similarity(f1: Dict, f2: Dict) -> float:
    """基于特征的相似度计算"""
    score = 0.0
    total_weight = 0.0
    
    # 角度重合度（权重 0.3）
    if f1["angles"] and f2["angles"]:
        common_angles = set(f1["angles"]) & set(f2["angles"])
        angle_sim = len(common_angles) / max(len(set(f1["angles"]) | set(f2["angles"])), 1)
        score += angle_sim * 0.3
    total_weight += 0.3
    
    # 数字重合度（权重 0.2）
    if f1["side_lengths"] and f2["side_lengths"]:
        common_nums = set(f1["side_lengths"]) & set(f2["side_lengths"])
        num_sim = len(common_nums) / max(len(set(f1["side_lengths"]) | set(f2["side_lengths"])), 1)
        score += num_sim * 0.2
    total_weight += 0.2
    
    # 模型关键词重合度（权重 0.4）
    if f1["models"] and f2["models"]:
        common_models = set(f1["models"]) & set(f2["models"])
        model_sim = len(common_models) / max(len(set(f1["models"]) | set(f2["models"])), 1)
        score += model_sim * 0.4
    total_weight += 0.4
    
    # 结论类型重合度（权重 0.1）
    if f1["conclusions"] and f2["conclusions"]:
        common_conc = set(f1["conclusions"]) & set(f2["conclusions"])
        conc_sim = len(common_conc) / max(len(set(f1["conclusions"]) | set(f2["conclusions"])), 1)
        score += conc_sim * 0.1
    total_weight += 0.1
    
    return score / total_weight if total_weight > 0 else 0.0


def combined_similarity(q1: Dict, q2: Dict) -> float:
    """综合相似度 = 0.4*文本相似度 + 0.6*特征相似度"""
    t_sim = text_similarity(q1["题目"], q2["题目"])
    f_sim = feature_similarity(
        extract_features(q1["题目"]), 
        extract_features(q2["题目"])
    )
    return 0.4 * t_sim + 0.6 * f_sim


def check_similarity(new_question: Dict, target_difficulty: str) -> Tuple[float, str, float]:
    """
    检查新题与现有题库的相似度
    返回: (最高相似度, 最相似题目编号, 综合相似度)
    """
    threshold = SIMILARITY_THRESHOLDS.get(target_difficulty, 0.70)
    
    best_match_id = None
    best_similarity = 0.0
    
    for qid, qdata in EXISTING_QUESTIONS.items():
        if qdata["难度"] != target_difficulty:
            continue
        
        sim = combined_similarity(new_question, qdata)
        if sim > best_similarity:
            best_similarity = sim
            best_match_id = qid
    
    return best_similarity, best_match_id, threshold


def evaluate_new_question(new_question: Dict, target_difficulty: str) -> Dict:
    """
    评估一道新题是否应该加入题库
    返回评估结果和建议
    """
    max_sim, best_match, threshold = check_similarity(new_question, target_difficulty)
    
    result = {
        "题目": new_question["题目"],
        "目标难度": target_difficulty,
        "最高相似度": round(max_sim, 3),
        "最相似题": best_match,
        "阈值": threshold,
        "判定": None,
        "建议": None,
    }
    
    if max_sim >= threshold:
        result["判定"] = "REJECT"  # 重复，不添加
        result["建议"] = f"与{best_match}相似度{max_sim:.1%} >= 阈值{threshold:.1%}，不添加"
    else:
        result["判定"] = "PASS"  # 可添加
        result["建议"] = f"与{best_match}相似度{max_sim:.1%} < 阈值{threshold:.1%}，可添加"
    
    return result


def get_difficulty_target_count(difficulty: str) -> int:
    """各难度目标题数"""
    counts = {"⭐": 17, "⭐⭐": 39, "⭐⭐⭐": 54, "⭐⭐⭐⭐": 35, "⭐⭐⭐⭐⭐": 35}
    return counts.get(difficulty, 0)


def get_current_count(difficulty: str) -> int:
    """统计当前题库中各难度实际题数"""
    return sum(1 for q in EXISTING_QUESTIONS.values() if q["难度"] == difficulty)


